matches = re.finditer(r'static const uint8_t PROGMEM (\w+)\[\] = \{\s*([^}]+)\};', code)
out = ""

for m in matches:
    name = m.group(1)
    bytes_str = m.group(2).replace(',', ' ').replace('\n', ' ')
    hex_vals = [int(b, 16) for b in bytes_str.split() if b.startswith('0x')]
    
    # hex_vals is 32 bytes (16x16 pixels).
    # Adafruit format: 2 bytes per row. 16 rows. Top-to-bottom, Left-to-right.
    # bit 7 of byte 0 is x=0, y=0.
    
    pixels = [[0]*16 for _ in range(16)]
    for r in range(16):
        b1 = hex_vals[r*2]
        b2 = hex_vals[r*2 + 1]
        for c in range(8):
            pixels[r][c] = (b1 >> (7-c)) & 1
            pixels[r][c+8] = (b2 >> (7-c)) & 1
            
    # Convert to GyverOLED format (vertical bytes)
    # 16x16 -> 2 pages of 16 columns.
    gyver_bytes = []
    for page in range(2):
        for c in range(16):
            val = 0
            for r in range(8):
                val |= (pixels[page*8 + r][c] << r)
            gyver_bytes.append(val)
            
    out += f"static const uint8_t PROGMEM {name}[] = {{\n    "
    for i in range(32):
        out += f"0x{gyver_bytes[i]:02X},"
        if i % 8 == 7: out += " "
        if i % 16 == 15 and i != 31: out += "\n    "
    out = out.rstrip(', ') + "\n};\n\n"
    
print(out)
